<?php

namespace eZ\Publish\SPI\Asset;

/**
 * This class represents a definition for an asset variant.
 *
 * It contains a list of filters which are applied to the $source variant.
 *
 * @package eZ\Publish\SPI\Asset
 */
class VariantDefinition
{
    /**
     * Unique identifier of this variant.
     *
     * Note that "original" is reserved.
     *
     * @var string
     */
    public $identifier;

    /**
     * Identifier of the source variant which is used as the basis for this one.
     *
     * @var string
     */
    public $source = 'original';

    /**
     * If the variant should be generated by default.
     *
     * @var bool
     */
    public $alwaysAvailable;

    /**
     * Storage to be used to store this variant.
     *
     * @var string
     */
    public $storageIdentifier;

    /**
     * Array of filters to apply in a sequence on $source to generate this variant.
     *
     * @var \eZ\Publish\SPI\Asset\FilterDefinition[]
     */
    public $filters;
}
